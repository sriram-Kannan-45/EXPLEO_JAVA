package ControlFlow;

public class if_elseif_else
{
	public static void main(String str[])
	{
		int color = 2;
		
		if (color == 1)
		{
			System.out.println("color blue");
		}
		else if (color == 2)
		{
			System.out.println("color red");
		}
		else
		{
			System.out.println("color green");
		}
	}
	
}
