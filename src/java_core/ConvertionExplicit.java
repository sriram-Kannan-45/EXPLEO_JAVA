package java_core;

public class ConvertionExplicit {

	public static void main(String[] args) {
		
		double d = 100.04; // double
		
		long l = (long) d; // double to long convertion 
		
		int i= (int) l; // long to int convertion
		
		System.out.println("Double Value :"+d);
		System.out.println("Long value :"+l);
		System.out.println("Int value :"+i);
	}

}
